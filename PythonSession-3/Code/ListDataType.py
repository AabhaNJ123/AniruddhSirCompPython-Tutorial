# Python Programm to understand how to write List Data type
# Lists are mutable
# List Index starts form 0 in Python.
""""
All the items in a list do not need to be of the same type
"""
a = [5,10,15,2.8,25,"Abc",35,'A'] #List
# a[2] = 15
print("a[2] = ", a[2])
# a[0:3] =  [5, 10, 15]
print("a[0:3] = ", a[0:3])
# a[5:] =  ['Abc', 35, 'A']
print("a[5:] = ", a[5:])
